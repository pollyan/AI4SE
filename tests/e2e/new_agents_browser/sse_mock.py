def build_agent_sse_response(
    request_body: dict[str, Any],
    *,
    turn_index: int = 0,
) -> str:
    workflow_id = request_body["workflowId"]
    stage_id = request_body["stageId"]
    run_id = request_body.get("runId")
    payloads = (
        HANDOFF_TEST_DESIGN_STAGE_PAYLOADS
        if run_id == "mock-run-test_design-handoff"
        else STAGE_PAYLOADS
    )
    payload = payloads[(workflow_id, stage_id)]
    should_request_next_stage = (
        payload.next_stage_id is not None
        and turn_index >= payload.turns_before_transition
    )
    output: dict[str, Any] = {
        "chat": (
            payload.chat
            if should_request_next_stage or not payload.next_stage_id
            else payload.clarification_chat
            or "当前阶段产出物已更新。我还需要你补充确认关键信息后，才会建议进入下一阶段。"
        ),
        "artifact_update": {
            "type": "replace",
            "markdown": payload.markdown,
        },
        "stage_action": None,
        "warnings": [],
    }
    if should_request_next_stage:
        output["stage_action"] = {
            "type": "request_next_stage",
            "target_stage_id": payload.next_stage_id,
        }
    run_started_event = {
        "type": "run_started",
        "runId": request_body.get("runId") or f"mock-run-{workflow_id.lower()}",
        "warnings": [],
    }
    event = {"type": "agent_turn", "output": output}
    return (
        f"data: {json.dumps(run_started_event, ensure_ascii=False)}\n\n"
        f"data: {json.dumps(event, ensure_ascii=False)}\n\n"
        "data: [DONE]\n\n"
    )
